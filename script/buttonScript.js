function arrowDown1(){
    var au = document.querySelector(".au1")
    var ad = document.querySelector(".ad1")
    if(ad.style.display=="block"){
        ad.style.display = 'none'
        au.style.display = 'block'
    }else{
        au.style.display = 'none'
        ad.style.display = 'block'
    }
}
